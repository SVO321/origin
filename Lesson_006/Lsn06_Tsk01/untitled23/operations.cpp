#include "operations.h"
double Addition (double number_one, double number_two) {
    return (number_one + number_two);
}
double Subtraction (double number_one, double number_two) {
    return (number_one - number_two);
}
double Multiplication (double number_one, double number_two) {
    return (number_one * number_two);
}
double Division (double number_one, double number_two) {
    return (number_one / number_two);
}
double pow(double number_one, double number_two) {
    return pow (number_one, number_two);
}
